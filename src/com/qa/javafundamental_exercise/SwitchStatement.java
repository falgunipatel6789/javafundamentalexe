package com.qa.javafundamental_exercise;

public class SwitchStatement {

	public static void main(String[] args) {
     byte day = 5 ;
     switch(day) {
     case 1:  System.out.println("day1 task ");
     break;
     case 2:System.out.println("day2 task ");
     break;
     case 3:System.out.println("day3 task ");
     break;
     case 4:System.out.println("day4 task ");
     break;
     case 5:System.out.println("day5 task ");
     break;
     case 6:System.out.println("day6 task ");
     break;
     case 7 :
     System.out.println("day 7 task");
     break;
     default :
    	 System.out.println("default");
     }
	}

}
